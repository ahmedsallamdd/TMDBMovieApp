import Foundation

struct TvShow: Codable {
    let adult: Bool?
    let backdropPath: String?
    let genreIDS: [Int]?
    let id: Int?
    let originCountry: [String]?
    let originalLanguage, originalName, overview: String?
    let popularity: Double?
    let posterPath, firstAirDate, name: String?
    let voteAverage: Double?
    let voteCount: Int?
    
    var genresAsString: String?
    var fullPosterLink: String { "https://image.tmdb.org/t/p/w185\(self.posterPath ?? "")" }
    
    enum CodingKeys: String, CodingKey {
        case adult
        case backdropPath = "backdrop_path"
        case genreIDS = "genre_ids"
        case id
        case originCountry = "origin_country"
        case originalLanguage = "original_language"
        case originalName = "original_name"
        case overview, popularity
        case posterPath = "poster_path"
        case firstAirDate = "first_air_date"
        case name
        case voteAverage = "vote_average"
        case voteCount = "vote_count"
    }
    
    func getGenresAsString(using allGenres: [Genre]) -> String {
        var genreAsString = ""
        
        guard let genreIDS, genreIDS.count > 0, allGenres.count > 0 else {
            return genreAsString
        }
        
        let filteredGenres = allGenres.filter({ genreIDS.contains($0.id) })
            
        genreAsString = filteredGenres.map({ $0.name }).joined(separator: ",")
        return genreAsString
    }
}

